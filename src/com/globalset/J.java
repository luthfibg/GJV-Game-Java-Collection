package com.globalset;

import java.util.Arrays;

public class J {
    public static void print(String text){
        System.out.print(text);
    }
    public static void println(String text){
        System.out.println(text);
    }
    public static void printIn(int integer){
        System.out.print(integer);
    }
    public static void printlIn(int integer){
        System.out.println(integer);
    }
    public static void printc(char ch){
        System.out.print(ch);
    }
    public static void printlc(char[][] ch){
        System.out.println(Arrays.deepToString(ch));
    }

}
